Modelo MVC para criação de softwares
O que é o padrão MVC?
O padrão MVC é utilizado para o desenvolvimento limpo e organizado de aplicações.
M -> Model: representa os dados e as regras do negócio. Exemplo: Aluno.php (CRUD) e AlunoDAO.php
V -> View: cuida parte gráfica e interface com o usuário, ou seja, o que aparece na tela. Exemplo: index.php ou index.html
C -> Controller: é o intermédiário entre a interface e os dados e regras de negócio. Exemplo: AlunoController.php